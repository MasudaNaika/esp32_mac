static_assert(sizeof(kPvSonyDriver) <= kDriverCapacity,
              "PV Sony driver no longer fits in the ROM resource");

constexpr size_t kPvAddressOffsets[] = {48, 114, 126, 170};

constexpr bool addressFieldsAreZero() {
    for (size_t offset : kPvAddressOffsets) {
        for (size_t byte = 0; byte < sizeof(uint32_t); ++byte) {
            if (kPvSonyDriver[offset + byte] != 0) {
                return false;
            }
        }
    }
    return true;
}

static_assert(addressFieldsAreZero(),
              "PV address offsets do not point to zero placeholders");

void writeBigEndian32(uint8_t *destination, uint32_t value) {
    destination[0] = (uint8_t)(value >> 24);
    destination[1] = (uint8_t)(value >> 16);
    destination[2] = (uint8_t)(value >> 8);
    destination[3] = (uint8_t)value;
}

