// Lookup table: byte → 8 RGB565 pixels (expand 1-bit to 16-bit)
static uint16_t lut[256][8];

// Chunk buffer in internal SRAM: 8 rows x 480 cols x 2 bytes = 7680 bytes
#define CHUNK_ROWS 8
static uint16_t chunkBuf[CHUNK_ROWS * IMG_W];

// Display task state
static volatile uint8_t *dispFb = NULL;
static volatile bool dispReady = false;

static void buildLut() {
    for (int b = 0; b < 256; b++) {
        for (int bit = 0; bit < 8; bit++) {
            lut[b][bit] = (b & (0x80 >> bit)) ? COLOR_BLACK : COLOR_WHITE;
        }
    }
}

static void dispTaskFunc(void *param) {
    printf("DISP: task running on core %d\n", xPortGetCoreID());

    while (1) {
        if (!dispFb || !panel_handle) {
            vTaskDelay(10);
            continue;
        }

        const uint8_t *mem = (const uint8_t *)dispFb;

        for (int chunk = 0; chunk < IMG_H; chunk += CHUNK_ROWS) {
            int byteIdx = chunk >> 3;

            for (int col = 0; col < IMG_W; col++) {
                int my = (MAC_FB_H - 1) - col;
                uint8_t byte = mem[my * MAC_FB_STRIDE + byteIdx];
                const uint16_t *expanded = lut[byte];

                chunkBuf[0 * IMG_W + col] = expanded[0];
                chunkBuf[1 * IMG_W + col] = expanded[1];
                chunkBuf[2 * IMG_W + col] = expanded[2];
                chunkBuf[3 * IMG_W + col] = expanded[3];
                chunkBuf[4 * IMG_W + col] = expanded[4];
                chunkBuf[5 * IMG_W + col] = expanded[5];
                chunkBuf[6 * IMG_W + col] = expanded[6];
                chunkBuf[7 * IMG_W + col] = expanded[7];
            }

            esp_lcd_panel_draw_bitmap(panel_handle, 0, chunk,
                                      IMG_W, chunk + CHUNK_ROWS,
                                      chunkBuf);
        }

        vTaskDelay(1);  // ~60fps max, yield between frames
    }
}

void dispInit() {
    printf("DISP: 1:1 rotated %dx%d (full screen), chunk buf %d bytes\n",
           IMG_W, IMG_H, (int)sizeof(chunkBuf));
    buildLut();

    // Start display rendering task on core 1
    xTaskCreatePinnedToCore(dispTaskFunc, "disp", 4096, NULL, 3, NULL, 1);
}

void dispDraw(uint8_t *mem) {
    // Just update the pointer — the display task on core 1 renders continuously
    dispFb = mem;
}

// Draw a multi-line status message centered on the LCD (scale 2x, rotated 90° CW)
// Logical landscape: 640 wide x 480 tall, mapped onto 480x640 panel via CW rotation.
void dispShowMessage(const char *lines[], int nlines) {
    if (!panel_handle) return;

    const int logW = DISP_H;  // 640 logical width
    const int logH = DISP_W;  // 480 logical height
    const int scale = 2;
    const int charW = 8 * scale;
    const int charH = 8 * scale;
    const int lineSpacing = charH + 4;
    int totalH = nlines * lineSpacing;
    int startY = (logH - totalH) / 2;

    // Allocate a column buffer: one panel column = DISP_H pixels tall
    // We render one logical text column at a time as a panel row-strip
    // Strategy: render full image into a 1-bit bitmap, then scan out rotated.

    // Simpler: build a small framebuffer in PSRAM, render text, then scan out rotated.
    // Use 1 bit per pixel: logW * logH / 8 = 640*480/8 = 38400 bytes
    int fbSize = logW * logH / 8;
    uint8_t *fb = (uint8_t *)heap_caps_malloc(fbSize, MALLOC_CAP_SPIRAM);
    if (!fb) return;
    memset(fb, 0, fbSize);  // all black (0 = black in our LUT)

    // Render text into 1-bit framebuffer (1 = white pixel)
    for (int l = 0; l < nlines; l++) {
        const char *text = lines[l];
        int len = strlen(text);
        int startX = (logW - len * charW) / 2;
        if (startX < 0) startX = 0;
        int y0 = startY + l * lineSpacing;

        for (int ci = 0; ci < len; ci++) {
            int ch = text[ci];
            if (ch < 32 || ch > 126) ch = 32;
            const uint8_t *glyph = font8x8[ch - 32];

            for (int row = 0; row < 8; row++) {
                uint8_t bits = glyph[row];
                for (int bit = 0; bit < 8; bit++) {
                    if (bits & (0x80 >> bit)) {
                        // Set scaled pixels
                        for (int sy = 0; sy < scale; sy++) {
                            for (int sx = 0; sx < scale; sx++) {
                                int px = startX + ci * charW + (7 - bit) * scale + sx;
                                int py = y0 + row * scale + sy;
                                if (px >= 0 && px < logW && py >= 0 && py < logH) {
                                    fb[(py * logW + px) / 8] |= (0x80 >> ((py * logW + px) % 8));
                                }
                            }
                        }
                    }
                }
            }
        }
    }

    // Scan out rotated 90° CW: panel(px, py) = logical(py, logH-1-px)
    // Same rotation as dispTaskFunc uses for the Mac framebuffer
    for (int chunk = 0; chunk < DISP_H; chunk += CHUNK_ROWS) {
        for (int col = 0; col < DISP_W; col++) {
            int logY = (logH - 1) - col;
            for (int r = 0; r < CHUNK_ROWS; r++) {
                int logX = chunk + r;
                int idx = logY * logW + logX;
                bool set = fb[idx / 8] & (0x80 >> (idx % 8));
                chunkBuf[r * DISP_W + col] = set ? COLOR_WHITE : COLOR_BLACK;
            }
        }
        esp_lcd_panel_draw_bitmap(panel_handle, 0, chunk, DISP_W, chunk + CHUNK_ROWS, chunkBuf);
    }

    free(fb);
}
