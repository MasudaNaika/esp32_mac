bool pvSonyPatchRom(uint8_t *rom, size_t romSize) {
    if (!rom || romSize < kSonyDriverOffset + kDriverCapacity) {
        printf("FDD ROM PATCH: ROM buffer is too small\n");
        return false;
    }
    if (memcmp(rom + kSonyDriverOffset,
               kSonyDriverSignature,
               sizeof(kSonyDriverSignature)) != 0) {
        printf("FDD ROM PATCH: .Sony resource signature mismatch at 0x%05X\n",
               (unsigned)kSonyDriverOffset);
        return false;
    }

    memcpy(rom + kSonyDriverOffset, kPvSonyDriver, sizeof(kPvSonyDriver));
    for (size_t offset : kPvAddressOffsets) {
        writeBigEndian32(rom + kSonyDriverOffset + offset, PV_FDD_GUEST_ADDR);
    }
    printf("FDD ROM PATCH: installed original PV driver (%u bytes at 0x%05X)\n",
           (unsigned)sizeof(kPvSonyDriver), (unsigned)kSonyDriverOffset);
    return true;
}
